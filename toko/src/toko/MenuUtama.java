    private void jMenuItem3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem3ActionPerformed
        // TODO add your handling code here:
        try (InputStream reportStream = getClass().getResourceAsStream("DataBarang.jasper")) {
            // Validasi keberadaan file
            if (reportStream == null) {
                JOptionPane.showMessageDialog(null, "File report1.jasper tidak ditemukan!");
                return;
            }

            // Mengisi laporan JasperReport
            JasperPrint jp = JasperFillManager.fillReport(
                reportStream,
                null,
                new koneksi().con // Koneksi database
            );

            // Menampilkan laporan menggunakan JasperViewer
            JasperViewer.viewReport(jp, false);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
            e.printStackTrace(); // Debugging
        }

    }//GEN-LAST:event_jMenuItem3ActionPerformed

